import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

import java.util.List;

@Data
@AllArgsConstructor
@NoArgsConstructor
@JsonIgnoreProperties(ignoreUnknown = true)
@Builder
public class Channel {

    @JacksonXmlElementWrapper(useWrapping = false)
    @JacksonXmlProperty
    private List<Item> item;
}
